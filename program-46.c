// increment or decrement befor and after in c program.....
#include <stdio.h>

int main(){
    int x = 5;
    printf("value of x is: %d \n", x++);
    printf("value of x is: %d \n", ++x);

    x = 5;
    printf("value of x is: %d \n", ++x);
    printf("value of x is: %d \n", x++);

    x = 5;
    printf("value of x is: %d \n", x--);
    printf("value of x is: %d \n", --x);

    x = 5;
    printf("value of x is: %d \n", --x);
    printf("value of x is: %d \n", x--);


return 0;
}
